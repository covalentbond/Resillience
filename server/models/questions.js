const mongoose = require('mongoose');

const questionSchema = new mongoose.Schema({
    questionId:{
        type: Number,
        required: true,
        unique: true
    },
    questionImage:{
        type: String,
        required: true
    },
    correctOption:{
        type: Number,
        required: true
    },
    selectedOption:{
        type: Number,
        default: 5
    },
    questionType:{
        type: String,
        required: true
    }
},{timestamps:true})


mongoose.model("Question",questionSchema);